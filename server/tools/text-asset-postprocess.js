const fs = require("fs/promises");
const path = require("path");

async function listFilesRecursive(dir) {
  const files = [];
  async function walk(current) {
    let entries = [];
    try {
      entries = await fs.readdir(current, { withFileTypes: true });
    } catch {
      return;
    }
    for (const entry of entries) {
      const fullPath = path.join(current, entry.name);
      if (entry.isDirectory()) {
        await walk(fullPath);
      } else if (entry.isFile()) {
        files.push(fullPath);
      }
    }
  }
  await walk(dir);
  return files;
}

function classifyTextAsset(buffer) {
  if (!buffer.length || buffer.includes(0)) return "";
  const text = buffer.toString("utf8").trim();
  if (!text) return "";

  if ((text.startsWith("{") || text.startsWith("[")) && text.length >= 2) {
    try {
      JSON.parse(text);
      return ".json";
    } catch {
      // Some Unity TextAssets are JSON-like templates with comments or trailing commas.
    }
  }

  if (/^<!doctype\s+html/i.test(text) || /^<html[\s>]/i.test(text)) return ".html";
  if (/^\s*(function|var |let |const |\(\s*function|window\.|!function|import |export )/i.test(text)) return ".js";
  if (/\/\*!?\s*JSZip\b/i.test(text) || /Generated by esbuild/i.test(text)) return ".js";
  if (/@media\s|^[.#][\w-]+\s*\{|^\s*(body|html|:root)\s*\{/i.test(text)) return ".css";
  return "";
}

function lettersToIndex(value) {
  return value.split("").reduce((total, char) => total * 26 + (char.charCodeAt(0) - 64), 0);
}

function parseLevelAssetName(name) {
  const match = /^Level(?:(\d+)|_?([A-Za-z][\w-]*))(?:_(\d+))?$/i.exec(name);
  if (!match) return null;
  return {
    level: match[1] ? Number(match[1]) : match[2] || null,
    variant: match[3] ? Number(match[3]) : null,
  };
}

function reconstructLevelJson(filePath, buffer) {
  if (!buffer.length || buffer.includes(0)) return null;
  const parsed = path.parse(filePath);
  const cleanName = parsed.ext === ".txt" ? parsed.name : parsed.base;
  const assetName = parseLevelAssetName(cleanName);
  if (!assetName) return null;

  const text = buffer.toString("utf8").trim();
  if (!text) return null;
  const rawLines = text.split(/\r?\n/).map((line) => line.trim()).filter(Boolean);
  if (!rawLines.length) return null;

  const cells = [];
  const grid = new Map();
  for (let index = 0; index < rawLines.length; index += 1) {
    const token = rawLines[index];
    const match = /^([A-Z]+)(\d+)(?:-(\d+))?$/.exec(token);
    if (!match) return null;

    const row = match[1];
    const column = Number(match[2]);
    const split = match[3] ? Number(match[3]) : null;
    const rowIndex = lettersToIndex(row);
    const cell = {
      index,
      code: token,
      row,
      rowIndex,
      column,
      split,
    };
    cells.push(cell);

    if (!grid.has(row)) grid.set(row, new Map());
    const columns = grid.get(row);
    if (!columns.has(column)) columns.set(column, []);
    columns.get(column).push(token);
  }

  const maxRowIndex = Math.max(...cells.map((cell) => cell.rowIndex));
  const maxColumn = Math.max(...cells.map((cell) => cell.column));
  const minRowIndex = Math.min(...cells.map((cell) => cell.rowIndex));
  const minColumn = Math.min(...cells.map((cell) => cell.column));
  const rows = [...grid.entries()]
    .sort((a, b) => lettersToIndex(a[0]) - lettersToIndex(b[0]))
    .map(([row, columns]) => ({
      row,
      rowIndex: lettersToIndex(row),
      columns: [...columns.entries()]
        .sort((a, b) => a[0] - b[0])
        .map(([column, codes]) => ({ column, codes })),
    }));

  return Buffer.from(`${JSON.stringify({
    format: "reconstructed-unity-level-grid-v1",
    note: "Recovered from a Unity TextAsset where each source line is a level cell code.",
    sourceAsset: parsed.base,
    name: cleanName,
    level: assetName.level,
    variant: assetName.variant,
    cellCount: cells.length,
    bounds: {
      minRowIndex,
      maxRowIndex,
      minColumn,
      maxColumn,
      width: maxColumn - minColumn + 1,
      height: maxRowIndex - minRowIndex + 1,
    },
    cells,
    grid: { rows },
    rawLines,
  }, null, 2)}\n`, "utf8");
}

async function uniqueSiblingPath(sourcePath, extension, buffer) {
  const parsed = path.parse(sourcePath);
  const cleanName = parsed.ext === ".txt" ? parsed.name : parsed.base;
  let target = path.join(parsed.dir, `${cleanName}${extension}`);
  let index = 1;
  while (true) {
    try {
      const existing = await fs.readFile(target);
      if (Buffer.compare(existing, buffer) === 0) return "";
      target = path.join(parsed.dir, `${cleanName}_${index}${extension}`);
      index += 1;
    } catch {
      return target;
    }
  }
}

async function postProcessTextAssets(rootDir) {
  const textAssetDir = path.join(rootDir, "TextAsset");
  const files = await listFilesRecursive(textAssetDir);
  const summary = {
    scanned: 0,
    created: 0,
    byExtension: {},
  };

  for (const file of files) {
    if (file.endsWith(".meta")) continue;
    const extension = path.extname(file).toLowerCase();
    if ([".json", ".js", ".css", ".html"].includes(extension)) continue;
    const buffer = await fs.readFile(file);
    const detected = classifyTextAsset(buffer);
    summary.scanned += 1;
    const recoveredLevelJson = detected ? null : reconstructLevelJson(file, buffer);
    const outputBuffer = recoveredLevelJson || buffer;
    const outputExtension = recoveredLevelJson ? ".json" : detected;
    if (!outputExtension) continue;
    const target = await uniqueSiblingPath(file, outputExtension, outputBuffer);
    if (!target) continue;
    await fs.writeFile(target, outputBuffer);
    summary.created += 1;
    summary.byExtension[outputExtension] = (summary.byExtension[outputExtension] || 0) + 1;
  }

  return summary;
}

module.exports = {
  classifyTextAsset,
  reconstructLevelJson,
  postProcessTextAssets,
};
